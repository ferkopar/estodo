--------------------------------------------------------
--  DDL for Trigger TRG2_USERS_DEFAULTS
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "TRG2_USERS_DEFAULTS" 
BEFORE INSERT OR UPDATE ON USERS_DEFAULTS FOR EACH ROW
BEGIN
  IF INSERTING
  THEN
    IF :new.USDEFAULTS_ID IS NULL OR :new.USDEFAULTS_ID = 0
      THEN
      SELECT SEQ_BASE.NEXTVAL
      INTO :NEW.USDEFAULTS_ID
      FROM DUAL;
      END IF;
      :new.CRD := SYSTIMESTAMP;      --LÉTREHOZÁS IDŐPONTJA 
      :NEW.CRU := NVL(V('APP_USER'), USER); --APEX USER AKI LÉTREHOZZA A REKORDOT
      ELSIF UPDATING
      THEN
      :new.MDD := SYSTIMESTAMP;      --MÓDOSÍTÁS IDŐPONTJA
      :NEW.MDU := NVL(V('APP_USER'), USER); --APEX USER AKI MÓDOSÍT
      END IF;
      END TRG_USERS_DEFAULTS;
/
ALTER TRIGGER "TRG2_USERS_DEFAULTS" ENABLE;
