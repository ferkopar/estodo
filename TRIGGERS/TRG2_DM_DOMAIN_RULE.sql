--  DDL for Trigger TRG2_DM_DOMAIN_RULE
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "TRG2_DM_DOMAIN_RULE" 
BEFORE INSERT OR UPDATE ON DM_DOMAIN_RULE FOR EACH ROW
BEGIN
  IF INSERTING
  THEN
    IF :new.DOMAIN_RULE_ID IS NULL OR :new.DOMAIN_RULE_ID = 0
      THEN
      SELECT SEQ_BASE.NEXTVAL
      INTO :NEW.DOMAIN_RULE_ID
      FROM DUAL;
      END IF;
      :new.CRD := SYSTIMESTAMP;      --LÉTREHOZÁS IDŐPONTJA 
      :NEW.CRU := NVL(V('APP_USER'), USER); --APEX USER AKI LÉTREHOZZA A REKORDOT
      ELSIF UPDATING
      THEN
      :new.MDD := SYSTIMESTAMP;      --MÓDOSÍTÁS IDŐPONTJA
      :NEW.MDU := NVL(V('APP_USER'), USER); --APEX USER AKI MÓDOSÍT
      END IF;
      END TRG_DM_DOMAIN_RULE;
/
ALTER TRIGGER "TRG2_DM_DOMAIN_RULE" ENABLE;
