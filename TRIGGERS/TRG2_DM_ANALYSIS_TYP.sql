--  DDL for Trigger TRG2_DM_ANALYSIS_TYP
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "TRG2_DM_ANALYSIS_TYP" 
BEFORE INSERT OR UPDATE ON DM_ANALYSIS_TYP FOR EACH ROW
BEGIN
  IF INSERTING
  THEN
    IF :new.ANALYSIS_TYPE_ID IS NULL OR :new.ANALYSIS_TYPE_ID = 0
      THEN
      SELECT SEQ_BASE.NEXTVAL
      INTO :NEW.ANALYSIS_TYPE_ID
      FROM DUAL;
      END IF;
      :new.CRD := SYSTIMESTAMP;      --LÉTREHOZÁS IDŐPONTJA 
      :NEW.CRU := NVL(V('APP_USER'), USER); --APEX USER AKI LÉTREHOZZA A REKORDOT
      ELSIF UPDATING
      THEN
      :new.MDD := SYSTIMESTAMP;      --MÓDOSÍTÁS IDŐPONTJA
      :NEW.MDU := NVL(V('APP_USER'), USER); --APEX USER AKI MÓDOSÍT
      END IF;
      END TRG_DM_ANALYSIS_TYP;
/
ALTER TRIGGER "TRG2_DM_ANALYSIS_TYP" ENABLE;
